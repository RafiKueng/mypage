<h4>Design:</h4>
<ul>
  <li>JavaScript based User Interface (using jQuery & jQuery UI)</li>
  <li>Python / Django based server side</li>
  <li>Celery based task system allowing a huge number of worker nodes</li>
  <li>Scalable: can run locally as a User Interface for glass as a large scale wweb application</li>
  <li>several parts: static web server, dynamic web server, task broker, workers (can all run on different machines)</li>
</ul>
